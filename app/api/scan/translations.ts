export function translateViolation(id: string): Translation {
  return translations[id] ?? {
    category: "Other",
    title: id.replace(/-/g, " ").replace(/\b\w/g, (c) => c.toUpperCase()),
    why: "This element has an accessibility issue that may affect users of assistive technologies.",
    fix: "Inspect this element in your browser's accessibility tree (DevTools > Accessibility panel) to understand the specific issue. Ensure the element has correct semantic HTML, appropriate ARIA attributes, and meets WCAG 2.1 AA standards.",
    effort: "Moderate",
    effortTime: "2–4 hours",
    wcagUrl: "https://www.w3.org/WAI/WCAG21/quickref/",
  };
}
